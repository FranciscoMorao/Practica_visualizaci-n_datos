"""## 3.17. Gráficas (curvas de Kaplan-Meier) por estadio tumoral"""

# =====================================================
# 17. Kaplan-Meier por estadio tumoral
# =====================================================

from lifelines import KaplanMeierFitter

df_km = df[
    [
        "Overall Survival (Months)",
        "Overall Survival Status",
        "Tumor Stage"
    ]
].dropna()

# Crear variable evento: 1 = fallecida, 0 = censurada
df_km["event"] = df_km["Overall Survival Status"].apply(
    lambda x: 1 if "deceased" in str(x).lower() or "died" in str(x).lower() else 0
)

kmf = KaplanMeierFitter()

plt.figure(figsize=(9, 6))

for stage in sorted(df_km["Tumor Stage"].unique()):
    data_stage = df_km[df_km["Tumor Stage"] == stage]

    if len(data_stage) > 10:
        kmf.fit(
            durations=data_stage["Overall Survival (Months)"],
            event_observed=data_stage["event"],
            label=f"Estadio {stage}"
        )
        kmf.plot_survival_function(ci_show=False)

plt.title("Curvas Kaplan-Meier de supervivencia por estadio tumoral")
plt.xlabel("Tiempo de seguimiento (meses)")
plt.ylabel("Probabilidad de supervivencia")
plt.grid(True)
guardar("17_kaplan_meier_estadio")

