"""## 3.13. Gráfica hacienndo un clustering visual con PCA"""

# =========================
# GRÁFICA 13: Clustering visual con PCA
# =========================

from sklearn.preprocessing import StandardScaler
from sklearn.decomposition import PCA
from sklearn.cluster import KMeans

vars_cluster = [
    "Age at Diagnosis",
    "Tumor Stage",
    "Tumor Size",
    "Mutation Count",
    "Nottingham prognostic index"
]

df_cluster = df[vars_cluster].dropna()

scaler = StandardScaler()
X_scaled = scaler.fit_transform(df_cluster)

pca = PCA(n_components=2)
X_pca = pca.fit_transform(X_scaled)

kmeans = KMeans(n_clusters=3, random_state=42, n_init=10)
clusters = kmeans.fit_predict(X_scaled)

df_pca = pd.DataFrame({
    "PCA1": X_pca[:, 0],
    "PCA2": X_pca[:, 1],
    "Cluster": clusters
})

fig = px.scatter(
    df_pca,
    x="PCA1",
    y="PCA2",
    color=df_pca["Cluster"].astype(str),
    title="Agrupación de pacientes mediante PCA y K-Means"
)

fig.write_html("graficas_metabric/13_pca_clusters.html")
fig.show()

